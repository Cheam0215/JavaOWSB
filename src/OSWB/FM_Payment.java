    private void populateTable() {
        List<PurchaseOrder> poList = financeManagerPOServices.getAllPOs();

        tableModel.setRowCount(0);

        int rowCount = 0;
        for (PurchaseOrder po : poList) {
            if (po != null) {
                tableModel.addRow(new Object[]{
                    po.getPoId(),
                    po.getPrId(),
                    po.getRaisedBy(),
                    po.getItemCode(),
                    po.getSupplierCode(),
                    po.getQuantity(),
                    po.getRequestedDate(),
                    po.getRequiredDate(),
                    po.getPaymentAmount(),
                    po.getStatus(),
                    po.getRemark()
                });
                rowCount++;
            }
        }
        System.out.println("Rows added to table: " + rowCount);
        tableModel.fireTableDataChanged(); 
        System.out.println("Table model row count after update: " + tableModel.getRowCount());
        jTable2.repaint();
    }
    private void setupTableSelection() {
        jTable2.getSelectionModel().addListSelectionListener(event -> {
            int selectedRow = jTable2.getSelectedRow();
            if (selectedRow >= 0) {
                String status = tableModel.getValueAt(selectedRow, 9).toString();
                if (status.equals(Status.REJECTED.toString()) || status.equals(Status.APPROVED.toString())) {
                    jTable2.clearSelection();
                    JOptionPane.showMessageDialog(this, "Only RECEIVED Purchase Orders can be selected.", 
                        "Invalid Selection", JOptionPane.WARNING_MESSAGE);
                }
                else if(status.equals(Status.PAID.toString())){
                    jTable2.clearSelection();
                    JOptionPane.showMessageDialog(this, "This Purchase Order was already PAID.", 
                        "PAID Purchase Order", JOptionPane.WARNING_MESSAGE);
                }
            }
        });
    }

